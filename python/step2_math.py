# step2_math.py

numbers = [5, -10, 15, -2, 8]

# abs(): 返回一个数的绝对值。
print(f"Absolute value of -10: {abs(-10)}")

# sum(): 计算可迭代对象中所有元素的和。
print(f"Sum of numbers {numbers}: {sum(numbers)}")

# min(): 返回可迭代对象中最小的元素。
print(f"Minimum of numbers: {min(numbers)}")

# max(): 返回可迭代对象中最大的元素。
print(f"Maximum of numbers: {max(numbers)}")

# round(): 将数字四舍五入到指定的精度。
pi_approx = 3.14159
print(f"Rounding {pi_approx} to 2 decimal places: {round(pi_approx, 2)}")

# pow(): 返回 base 的 exp 次方。
print(f"2 to the power of 3: {pow(2, 3)}")
